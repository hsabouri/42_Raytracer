#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int nbr;
	int ngt;

	nbr = 0;
	ngt = 0;
	i = 0;
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v') ||
			(str[i] == ' ') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		ngt = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
	{
		nbr *= 10;
		nbr += (int)str[i] - '0';
		i++;
	}
	if (ngt == 1)
		return (-nbr);
	else
		return (nbr);
}
